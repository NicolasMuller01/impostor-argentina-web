export const getRandomFact = (categoryIds) => {
  const randomCategoryId = categoryIds[Math.floor(Math.random() * categoryIds.length)];
  const category = FACT_CATEGORIES[randomCategoryId];
  const randomFact = category.facts[Math.floor(Math.random() * category.facts.length)];
  return { 
    categoryId: randomCategoryId, 
    category: category.name, 
    fact: randomFact,
    icon: category.icon
  };
};

export const getRandomFactsForPlayers = (playerCount, categoryIds) => {
  const facts = [];
  const usedFacts = new Set();
  
  for (let i = 0; i < playerCount; i++) {
    let attempts = 0;
    let fact;
    do {
      fact = getRandomFact(categoryIds);
      attempts++;
    } while (usedFacts.has(fact.fact) && attempts < 100);
    
    usedFacts.add(fact.fact);
    facts.push(fact);
  }
  
  return facts;
};

