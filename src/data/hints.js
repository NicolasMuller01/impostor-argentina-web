/**
 * Get a random hint for a specific word in a category.
 * Falls back to a generic hint if the word isn't found.
 */
export const getHintForWord = (categoryId, word) => {
    const categoryHints = HINTS_DATA[categoryId];
    if (!categoryHints) return "Secreto";

    const wordHints = categoryHints[word];
    if (!wordHints || wordHints.length === 0) return "Enigma";

    return wordHints[0];
};
